function formatNumber(value) {
  return new Intl.NumberFormat('en-US').format(Number(value) || 0)
}

function escapeHtml(value) {
  return String(value).replace(/[&<>'"]/g, (character) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', "'": '&#39;', '"': '&quot;' })[character])
}

